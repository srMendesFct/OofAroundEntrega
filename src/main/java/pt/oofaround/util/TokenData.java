package pt.oofaround.util;

public class TokenData {

	public String tokenID;
	public String usernameR;
	public String role;

	public TokenData() {
	}

	public TokenData(String tokenID, String usernameR, String role) {
		this.tokenID = tokenID;
		this.usernameR = usernameR;
		this.role = role;
	}

}
